
# Program to calculate the average of three grades

# Prompt the user to enter the first grade
grade1 = float(input("Enter your first grade: "))

# Prompt the user to enter the second grade
grade2 = float(input("Enter your second grade: "))

# Prompt the user to enter the third grade
grade3 = float(input("Enter your third grade: "))

# Calculate the average
average = (grade1 + grade2 + grade3) / 3

# Print the average
print("Your average grade is:", average)
